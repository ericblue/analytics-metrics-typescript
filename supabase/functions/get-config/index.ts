import "jsr:@supabase/functions-js/edge-runtime.d.ts"

import { serve } from 'https://deno.land/std@0.168.0/http/server.ts'
import { corsHeaders } from '../_shared/cors.ts'

console.log("Hello from Functions!")

serve(async (req) => {
  // Handle CORS
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    // Get the authorization header
    const authHeader = req.headers.get('Authorization')
    if (!authHeader) {
      throw new Error('Missing authorization header')
    }

    // Return configuration values
    const config = {
      GA_TRACKING_ID: Deno.env.get('GA_TRACKING_ID'),
      CLARITY_PROJECT_ID: Deno.env.get('CLARITY_PROJECT_ID'),
      POSTHOG_API_KEY: Deno.env.get('POSTHOG_API_KEY'),
      POSTHOG_HOST_URL: Deno.env.get('POSTHOG_HOST_URL'),
      CHATWOOT_WEBSITE_TOKEN: Deno.env.get('CHATWOOT_WEBSITE_TOKEN'),
      CHATWOOT_BASE_URL: Deno.env.get('CHATWOOT_BASE_URL')
    }

    return new Response(
      JSON.stringify(config),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      },
    )
  } catch (error) {
    return new Response(
      JSON.stringify({ error: error.message }),
      { 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 401,
      },
    )
  }
})

